# ═══════════════════════════ ВСПОМОГАТЕЛЬНЫЕ ════════════════════════════════

def _sp(para, line=360, before=0, after=0):
    pPr = para._p.get_or_add_pPr()
    s = pPr.find(qn('w:spacing'))
    if s is None:
        s = OxmlElement('w:spacing'); pPr.append(s)
    s.set(qn('w:line'), str(line))
    s.set(qn('w:lineRule'), 'auto')
    s.set(qn('w:before'), str(before))
    s.set(qn('w:after'), str(after))

def _ind(para, first=709):
    pPr = para._p.get_or_add_pPr()
    ind = pPr.find(qn('w:ind'))
    if ind is None:
        ind = OxmlElement('w:ind'); pPr.append(ind)
    ind.set(qn('w:firstLine'), str(first))

def _f(run, bold=False, sz=14):
    run.font.name = 'Times New Roman'
    run.font.size = Pt(sz)
    run.font.bold = bold
    run.font.color.rgb = RGBColor(0, 0, 0)
    rPr = run._r.get_or_add_rPr()
    rf = rPr.find(qn('w:rFonts'))
    if rf is None:
        rf = OxmlElement('w:rFonts'); rPr.insert(0, rf)
    for a in ('w:ascii','w:hAnsi','w:cs','w:eastAsia'):
        rf.set(qn(a), 'Times New Roman')


# ═══════════════════════════ СТРОИТЕЛЬНЫЕ БЛОКИ ═════════════════════════════

def p(doc, text, bold=False, indent=True):
    """Основной абзац."""
    pr = doc.add_paragraph()
    pr.alignment = WD_ALIGN_PARAGRAPH.JUSTIFY
    _sp(pr); _ind(pr, 709 if indent else 0)
    _f(pr.add_run(text), bold=bold)
    return pr

def h(doc, text, lv):
    """Заголовок: H1/H2 жирный, H3 обычный."""
    return p(doc, text, bold=(lv <= 2))

def em(doc):
    return p(doc, '')

def bl(doc, text, last=False):
    return p(doc, f'\u2013 {text}{"." if last else ";"}')

def nm(doc, n, text):
    t = text[0].upper() + text[1:]
    if not t.endswith('.'): t += '.'
    return p(doc, f'{n}. {t}')

def tc(doc, text):
    """Подпись таблицы: 14пт, одинарный, левый, без красной строки."""
    pr = doc.add_paragraph()
    pr.alignment = WD_ALIGN_PARAGRAPH.LEFT
    _sp(pr, line=240); _ind(pr, 0)
    _f(pr.add_run(text))

def fc(doc, text):
    """Подпись рисунка: центр, одинарный, без красной строки."""
    pr = doc.add_paragraph()
    pr.alignment = WD_ALIGN_PARAGRAPH.CENTER
    _sp(pr, line=240); _ind(pr, 0)
    _f(pr.add_run(text))

def fp(doc, label):
    """Серая заглушка для рисунка."""
    pr = doc.add_paragraph()
    pr.alignment = WD_ALIGN_PARAGRAPH.CENTER
    _sp(pr, line=240); _ind(pr, 0)
    r = pr.add_run(f'[== {label} ==]')
    _f(r, sz=12)
    r.font.color.rgb = RGBColor(153, 153, 153)

def tbl(doc, headers, rows):
    """Таблица: шапка 12пт центр, данные 12пт лево."""
    t = doc.add_table(rows=1+len(rows), cols=len(headers))
    t.style = 'Table Grid'
    def _c(cell, txt, center=False):
        pr = cell.paragraphs[0]; pr.clear()
        pr.alignment = WD_ALIGN_PARAGRAPH.CENTER if center else WD_ALIGN_PARAGRAPH.LEFT
        _sp(pr, line=240); _ind(pr, 0)
        _f(pr.add_run(str(txt)), sz=12)
    for i, hd in enumerate(headers):
        _c(t.rows[0].cells[i], hd, True)
    for ri, row in enumerate(rows):
        for ci, val in enumerate(row):
            _c(t.rows[ri+1].cells[ci], val)


